        <div class="modal-body container-fluid">	
				<div class="well">
					  <p><b>What is USI?</b></p>
					  <p>From 1 January 2015 students enrolling in
						nationally recognised Training in Australia will need a Unique Student
						Identifier (USI) and Training Providers will not be able to issue a Statement
						of Attainment without your USI on file.</p>
					  <p>The USI will link students to their
						training records. Students will be able to access their records online, download
						them and share them with future training organisations electronically. With the
						student's permission, training organisations will be able to see their
						student's entire training record from 2015, allowing for easier assessment of
						pre-requisites and credit transfers.</p>
					  <p><b>Who needs a USI?</b></p>
					  <ul><li><b>All students</b> who
						are enrolling in nationally recognised training in Australia.</li></ul>
					  <p><b>What does <span class='btn btn-small btn-success' style="float: none !important;">VERIFY</span> mean?</b></p>
					  <p>Not sure your USI is correct? You can verify your own USI by entering the following information when making a booking: </p>
						<ul>
						<li>USI (unique student identifier)</li>
						<li>Given name &amp; surname, as it appears on  your personal identification</li>
						<li>Date of birth</li>
						</ul>
					  <p><b>What does <span class='btn btn-small btn-primary' style="float: none !important;">CREATE</span> mean?</b></p>
					  <p>If you do not have a USI, you have 2 options:</p>
						<ul>
					  <li>Select <span class='btn btn-small btn-primary' style="float: none !important;">CREATE</span> - by selecting create, you are giving permission to The Coffee School to create a USI on your behalf. Or</li>
					  <li>You can create your own Unique Student
						Identifier at <a href="http://usi.gov.au/create-your-USI/Pages/default.aspx">http://usi.gov.au/create-your-USI/Pages/default.aspx</a></li>
						</ul>
					  <p><b><u>PLEASE NOTE, when making a booking:</u></b></p>
					  <p>It is<b> NOT COMPULSORY </b>to have your USI when making a booking. You can proceed
						with your booking &amp; obtain your USI at a later date.</p>
					  <p>You <b>MUST</b>, however, have your USI before attending the course.</p>
